#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;
	size_t	i;

	p = malloc(count * size);
	i = 0;
	if (p == NULL)
		return (p);
	while (i < size * count)
	{
		((unsigned char *)p)[i] = 0;
		++i;
	}
	return (p);
}
