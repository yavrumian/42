#include "libft.h"

size_t	ft_putstr(char *s)
{
	int	i;

	if (!s)
		return ft_putstr("(null)");
	i = 0;
	while (*(s + i) != '\0')
	{
		ft_putchar(s[i]);
		// write(1, s + i, 1);
		i++;
	}
	ft_putchar('\0');
	// write(1, "", 1);
	return (i);
}
