#include "libft.h"

size_t	ft_putnbr_u(uint n, size_t len)
{
	if (n >= 10)
		len = ft_putnbr_u(n / 10, len);
	n = n % 10 + '0';
	write(1, &n, 1);
	return (++len);
}
